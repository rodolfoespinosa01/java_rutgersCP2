
public class Time 
{
    private int hours;
    private int minutes;
    private int seconds;

    public Time() //1st constructor
    {
        hours = 0;
        minutes = 0;
        seconds = 0;
    }
    public Time(int hours, int minutes, int seconds) //2nd constructor
    {
        this.hours = hours;
        this.minutes = minutes;
        this.seconds = seconds;
    }


}
